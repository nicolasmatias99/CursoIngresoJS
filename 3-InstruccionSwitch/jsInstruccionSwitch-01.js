function mostrar()
{
	//tomo el mes
	let mesDelAño;

	mesDelAño = document.getElementById("txtIdMes").value;

	switch (mesDelAño){
		
		case "Enero":
		
			alert ("Que comiences bien el año");
			break;
			
		case "Marzo":	
			alert ("A calses!");
			break;

		case "Julio":
			alert ("Se vienen las vacaciones!");
			break;

		case "Diciembre":
			alert ("Felices fiestas!");
			break;
	}
	



}//FIN DE LA FUNCIÓN